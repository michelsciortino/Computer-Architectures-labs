; Reset Handler

Reset_Handler   PROC
                EXPORT  Reset_Handler             [WEAK]                                            
                LDR     R0, =Reset_Handler
				
				; your code here
Price_list	DCD 0x004, 120, 0x006, 315, 0x007, 1210, 0x00A, 245 
			DCD 0x010, 228, 0x012, 7, 0x016, 722, 0x017, 1217 
			DCD 0x018, 138, 0x01A, 2222, 0x01B, 34, 0x01E, 11
			DCD 0x022, 223, 0x023, 1249, 0x025, 240, 0x027, 112
			DCD 0x02C, 2245, 0x02D, 410, 0x031, 840, 0x033, 945
			DCD 0x036, 3211, 0x039, 112, 0x03C, 719, 0x03E, 661
			DCD 0x042, 230, 0x045, 1112, 0x047, 2627 , 0x04A, 265
	
Item_list 	DCD 0x022, 14, 0x006, 431, 0x03E, 1210, 0x017, 56342 

		MOV R0,	#8			;item struct size
		MOV R1,	#4			;number of elements in the list
		MOV R2,	#0			;current element
		MOV R10,#0			;tot=0
		MUL R9,R0,R1
		LDR R11,=Price_list
		LDR R12,=Item_list
Iteration
		MOV R3,	#0	;first
		MOV R4,	#27	;last
		MOV R5,	#0	;middle
		LDR R6,[R12,R2]
Bynary_search	
		cmp R3,R4
		BGT	Item_not_found
		ADD R5,R3,R4
		MOV R5,R5, LSR #1
		MUL R8,R5,R0 			;index
		LDR R7,[R11,R8]	  		;reading from matrix
		cmp R6,R7				;key check
		BEQ Use_Result
		ADDLT R4,R5,#-1
		ADDGT R3,R5,#+1
		B Bynary_search

Use_Result	
		ADD R8, #4
		ADD R2, #4
		LDR R6, [R12,R2]		;get item quantity
		LDR R7, [R11,R8]		;get item price
		MUL R8, R6, R7
		ADD R10, R8
		ADD R2, #4
		CMP R2,R9				;loop closing checking
		BEQ End_Program
		B Iteration

Item_not_found
		MOV R10,#0

End_Program
				
InfLoop         B      	InfLoop
                ENDP


